    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        
        List<Produtos>listar=DaoProduto.Listar();
        
        if(listar!=null){
              DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);
            
            for(Produtos produtos :listar){
                modelo.addRow(new String[]{
                    String.valueOf(produtos.getId()),
                  String.valueOf(produtos.getDescricao()),
                    String.valueOf(produtos.getQuantidade()),
                    String.valueOf(produtos.getValor())
                    
            });
                
            }
        }
                
        
        
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnExActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExActionPerformed
        
        int LinhaSelecionada = jTable1.getSelectedRow();
        if(LinhaSelecionada>0){
            String valorDado = jTable1.getValueAt(LinhaSelecionada, 0).toString();
            int id = Integer.parseInt(valorDado);
            boolean retorno = DaoProduto.Excluir(id);
            if(retorno){
                JOptionPane.showMessageDialog(this,"Nota excluÃ­da com sucesso!");
            }else{
                JOptionPane.showMessageDialog(this,"Falha na alteraÃ§Ã£o!");
            }
        }
        
        
    }//GEN-LAST:event_btnExActionPerformed

