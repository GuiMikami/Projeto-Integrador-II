    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        
        if(txtNomeProduto.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite o nome do produto");
        }
        
        if(txtEstoque.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite uma quantidade para o produto");
        }
   
        
        if(txtValorProduto.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite o valor de quanto devera custa o produto");
        }
        
        Produtos produto = new Produtos();
        
        produto.setDescricao(txtNomeProduto.getText());
        produto.setQuantidade(Integer.parseInt(txtEstoque.getText()));
        produto.setValor(txtValorProduto.getText());
        
        try {
            DaoProduto.SalvarP(produto);
            
            JOptionPane.showMessageDialog(this, "Feito");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "NÃ£o Feito  ");
        }
        
       
        
        
    }//GEN-LAST:event_btnIncluirActionPerformed

